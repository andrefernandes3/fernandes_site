// Função para aplicar as traduções
function applyTranslations(lang) {
    const langData = translations[lang];
    if (!langData) return;

    // 1. Traduz texto (innerHTML)
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (langData[key]) {
            element.innerHTML = langData[key]; // Usei innerHTML para permitir tags como &copy;
        }
    });

    // 2. Traduz Placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (langData[key]) {
            element.setAttribute('placeholder', langData[key]);
        }
    });

    // 3. Atualiza Meta Tags SEO
    if (langData.meta_title) document.title = langData.meta_title;
    const metaDesc = document.querySelector('meta[name="description"]');
    if (metaDesc && langData.meta_description) metaDesc.setAttribute('content', langData.meta_description);
}

// Event Listeners
document.getElementById('languageSelector')?.addEventListener('change', function () {
    const lang = this.value;
    localStorage.setItem('selectedLanguage', lang);
    applyTranslations(lang);
    // Dispara evento para o Chat.js saber que mudou
    window.dispatchEvent(new Event('languageChanged'));
});

window.addEventListener('DOMContentLoaded', function () {
    const savedLang = localStorage.getItem('selectedLanguage') || 'pt';
    const languageSelector = document.getElementById('languageSelector');
    if (languageSelector) languageSelector.value = savedLang;
    applyTranslations(savedLang);
});